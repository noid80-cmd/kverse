// ─── Context ────────────────────────────────────────────────────────────────

const LanguageContext = createContext<{
  locale: Locale
  setLocale: (l: Locale) => void
}>({ locale: 'en', setLocale: () => {} })

export function LanguageProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<Locale>('ko')

  useEffect(() => {
    const saved = localStorage.getItem('kvers-locale') as Locale
    if (saved && translations[saved]) {
      setLocaleState(saved)
      document.documentElement.dir = RTL_LOCALES.includes(saved) ? 'rtl' : 'ltr'
      document.documentElement.lang = saved
    } else {
      const browser = navigator.language.slice(0, 2) as Locale
      const detected = translations[browser] ? browser : 'ko'
      setLocaleState(detected)
      document.documentElement.lang = detected
    }
  }, [])

  function setLocale(l: Locale) {
    setLocaleState(l)
    localStorage.setItem('kvers-locale', l)
    document.documentElement.dir = RTL_LOCALES.includes(l) ? 'rtl' : 'ltr'
    document.documentElement.lang = l
  }

  return (
    <LanguageContext.Provider value={{ locale, setLocale }}>
      {children}
    </LanguageContext.Provider>
  )
}

export function useLanguage() {
  return useContext(LanguageContext)
}

export function useT() {
  const { locale } = useLanguage()
  return (key: string, vars?: Record<string, string | number>): string => {
    let str = translations[locale]?.[key] ?? translations.ko[key] ?? key
    if (vars) {
      for (const [k, v] of Object.entries(vars)) {
        str = str.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v))
      }
    }
    return str
  }
}
